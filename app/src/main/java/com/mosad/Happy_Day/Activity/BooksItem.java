package com.mosad.Happy_Day.Activity;

public class BooksItem {

    public String id ;
    public String name ;
    public String hallname ;
    public String phoneno ;
    public String date ;


    public BooksItem(String id, String name, String hallname, String phoneno, String date) {
        this.id = id;
        this.name = name;
        this.hallname = hallname;
        this.phoneno = phoneno;
        this.date = date;
    }
}
